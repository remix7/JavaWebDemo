package com.remix.domain;

public class Persion {
	private String name;
	private String gendle;
	private boolean merry;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGendle() {
		return gendle;
	}
	public void setGendle(String gendle) {
		this.gendle = gendle;
	}
	public boolean isMerry() {
		return merry;
	}
	public void setMerry(boolean merry) {
		this.merry = merry;
	}
	
}
